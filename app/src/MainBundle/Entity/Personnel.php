<?php

namespace MainBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Personnel
 */
class Personnel
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $idFichePerso;

    /**
     * @var boolean
     */
    private $idJob;

    /**
     * @var string
     */
    private $horraire;

    /**
     * @var \DateTime
     */
    private $dateRecrutement;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set idFichePerso
     *
     * @param integer $idFichePerso
     * @return Personnel
     */
    public function setIdFichePerso($idFichePerso)
    {
        $this->idFichePerso = $idFichePerso;

        return $this;
    }

    /**
     * Get idFichePerso
     *
     * @return integer 
     */
    public function getIdFichePerso()
    {
        return $this->idFichePerso;
    }

    /**
     * Set idJob
     *
     * @param boolean $idJob
     * @return Personnel
     */
    public function setIdJob($idJob)
    {
        $this->idJob = $idJob;

        return $this;
    }

    /**
     * Get idJob
     *
     * @return boolean 
     */
    public function getIdJob()
    {
        return $this->idJob;
    }

    /**
     * Set horraire
     *
     * @param string $horraire
     * @return Personnel
     */
    public function setHorraire($horraire)
    {
        $this->horraire = $horraire;

        return $this;
    }

    /**
     * Get horraire
     *
     * @return string 
     */
    public function getHorraire()
    {
        return $this->horraire;
    }

    /**
     * Set dateRecrutement
     *
     * @param \DateTime $dateRecrutement
     * @return Personnel
     */
    public function setDateRecrutement($dateRecrutement)
    {
        $this->dateRecrutement = $dateRecrutement;

        return $this;
    }

    /**
     * Get dateRecrutement
     *
     * @return \DateTime 
     */
    public function getDateRecrutement()
    {
        return $this->dateRecrutement;
    }
}
