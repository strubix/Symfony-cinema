     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $idDistrib;

    /**
     * @var string
     */
    private $nom;

    /**
     * @var string
     */
    private $telephone;

    /**
     * @var string
     */
    private $adresse;

    /**
     * @var string
     */
    private $cpostal;

    /**
     * @var string
     */
    private $ville;

    /**
     * @var string
     */
    private $pays;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

