     * Set idDistrib
     *
     * @param integer $idDistrib
     * @return Distrib
     */
    public function setIdDistrib($idDistrib)
    {
        $this->idDistrib = $idDistrib;

        return $this;
    }

    /**
     * Get idDistrib
     *
     * @return integer 
